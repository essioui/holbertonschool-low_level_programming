#include "3-calc.h"

/**
 * op_add - fuction addition
 * @a: integer
 * @b: integer
 * Return: add
*/
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - fuction substraction
 * @a: integer
 * @b: integer
 * Return: sub
*/
int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - fuction multiplication
 * @a: integer
 * @b: integer
 * Return: mul
*/
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - fuction division
 * @a: integer
 * @b: integer
 * Return: div
*/
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - fuction modulation
 * @a: integer
 * @b: integer
 * Return: mod
*/
int op_mod(int a, int b)
{
    return (a % b);
}